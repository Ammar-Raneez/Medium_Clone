export const getServerSideProps = async (context) => {
  const query = `
    *[_type == 'post'] {
      _id,
      author -> {
        name,
        image
      },
      description,
      mainImage,
      slug
    }
  `;

  const posts = await sanityClient.fetch(query);
  return {
    props: {
      posts,
    }
  }
};
